# 4.2 F검정

# F검정
# 집단 간 분산(Between-groups Variability)과 집단 내 분산(Within-groups Variability)의
# 비로 계산되는 F값(F Value, F Statistic)을 가설검정을 위한 검정통계량으로 사용
# 집단 간 분산이 클수록, 집단 내 분산이 작을수록 집단평균이 다를 가능성이 증가

# F값(F Value, F Statistic)
# F=집단간분산/집단내분산
# <집단개수-1>, <표본크기-집단개수>를 자유도로 갖는 F분포(F Distribution)를 따름

# pf(F값,df1=자유도1,df=자유도2)
pf(9.59,1,8,lower.tail=F) # p-value:0.0147 (귀무가설 기각가능)

# 유의수준 0.05이하가 되는 q값
qf(0.05,1,8,lower.tail=F) # 5.317

