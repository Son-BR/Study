# ADHD 치료 예시

# 치료A, 치료B 집단 비교: 집단 간(Between-groups) 일원분산분석
# 치료기간 4주, 16주 비교: 집단 내(Within-groups) 일원분산분석, 반복측정 분산분석(Repeated Measures ANOVA)
# 치료법x기간 비교: 이원분산분석, F검정3번(치료법, 기간. 치료법x기간)
# 스트레스변수(공변량, Covariate) 통제후 비교: 공분산분석(ANCOVA: analysis of covariance)
# ADHD점수 외의 측정지표 추가: 다변량 분산분석(MANOVA: multivariate analysis of variance, 두 개 이상의 종속변수를 포함)
# 위의 두개 모두 포함: 다변량 공분산분석(MANCOVA: multivariate analysis of covariance)

